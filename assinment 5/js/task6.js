// Q6. Write a function that will take an array and generate a mirror array of it.


var arr = [0, 5, 6, 7];
var marr = [7, 6, 5, 0];
function marr(arr) {
    reverses = arr.reverse(' ');
    return Array(arr + " , " + reverse);
}
document.write("Mirror([0,5,6,7]): " + arr + ", " + marr +"<br>"); 

